# Donne les coordonnées des monstres
def getSpawnPoints(structure) :
    if structure == "b1x2" :
        if random.randint(0,2) == 0 : 
            return [
                (224,160),
                (256,160)
            ]
        else :
            return [
                (160,128),
                (320,128),
                (160,192),
                (320,192)
            ]
    if structure == "b1x4" :
        if random.randint(0,2) == 0 : 
            return [
                (192,160),
                (288,160)
            ]
        else :
            return [
                (64,64),
                (384,64),
                (64,256),
                (384,256)
            ]
    if structure == "b2x5" :
        if random.randint(0,2) == 0 : 
            return [
                (240,128),
                (240,192)
            ]
        else :
            return [
                (64,64),
                (384,64),
                (64,256),
                (384,256)
            ]
    if structure == "b6x1" :
        if random.randint(0,2) == 0 : 
            return [
                (64,64),
                (384,64),
                (64,256),
                (384,256)
            ]
        else :
            return [
                (192,160),
                (288,160)
            ]
    if structure == "b6x2" :
        if random.randint(0,2) == 0 : 
            return [
                (240,128),
                (240,192)
            ]
        else :
            return [
                (64,64),
                (384,64),
                (64,256),
                (384,256)
            ]
    if structure == "d" :
        return [
            (160,96),
            (320,96),
            (160,224),
            (320,224)
        ]
    if structure == "t4" or structure == "t2N" or structure == "t2S":
        return [
            (240,128),
            (240,192),
            (192,160),
            (288,160)
        ]
    if structure == "c" :
        return [
           (160,160),
           (240,160),
           (320,160)
        ]